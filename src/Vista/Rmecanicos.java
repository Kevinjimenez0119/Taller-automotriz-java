public void mostrar()
    {
        DefaultTableModel modelo=new DefaultTableModel();
        ResultSet re=obj.obj.n2.llenartabla("select * from mecanicos");
        modelo.setColumnIdentifiers(new Object[]{"documento","nombres","apellidos","direccion","telefono","contraseña","sueldo"});
        try {
            while(re.next())
            {
                modelo.addRow(new Object[]{re.getString(1),re.getString(2),re.getString(3),re.getString(4),re.getString(5),re.getString(6),re.getString(7)});
            }
            tabla.setModel(modelo);
        } catch (Exception e) {
        }
    }
    public void mostrar2(String id)
    {
        DefaultTableModel modelo=new DefaultTableModel();
        ResultSet re=obj.obj.n2.llenartabla("select * from mecanicos where idmecanico='"+id+"'");
        modelo.setColumnIdentifiers(new Object[]{"documento","nombres","apellidos","direccion","telefono","tipo de cliente","contraseña","sueldo"});
        try {
            while(re.next())
            {
                modelo.addRow(new Object[]{re.getString(1),re.getString(2),re.getString(3),re.getString(4),re.getString(5),re.getString(6),re.getInt(7)});
            }
            tabla.setModel(modelo);
        } catch (Exception e) {
        }
    }
